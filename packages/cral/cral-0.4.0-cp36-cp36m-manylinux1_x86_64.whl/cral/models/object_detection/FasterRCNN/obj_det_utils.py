def log(text, array=None):
    """Prints a text message.

    And, optionally, if a Numpy array is provided it prints it's shape, min,
    and max values.
    """
    if array is not None:
        text = text.ljust(25)
        text += ('shape: {:20}  '.format(str(array.shape)))
        if array.size:
            text += ('min: {:10.5f}  max: {:10.5f}'.format(array.min(), array.max()))  # noqa: E501
        else:
            text += ('min: {:10}  max: {:10}'.format('', ''))
        text += '  {}'.format(array.dtype)
    print(text)


class BatchNorm(KL.BatchNormalization):
    """Extends the Keras BatchNormalization class to allow a central place to
    make changes if needed.

    Batch normalization has a negative effect on training if batches are small
    so this layer is often frozen (via setting in Config class) and functions
    as linear layer.
    """
    def call(self, inputs, training=None):
        """Note about training values:

        None: Train BN layers. This is the normal mode
        False: Freeze BN layers. Good when batch size is small
        True: (don't use). Set layer in training mode even when
        making inferences
        """
        return super(self.__class__, self).call(inputs, training=training)


